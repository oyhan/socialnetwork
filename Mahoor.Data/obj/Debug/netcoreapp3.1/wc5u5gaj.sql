CREATE EXTENSION IF NOT EXISTS postgis;

CREATE TABLE "AspNetRoles" (
    "Id" text NOT NULL,
    "Name" character varying(256) NULL,
    "NormalizedName" character varying(256) NULL,
    "ConcurrencyStamp" text NULL,
    CONSTRAINT "PK_AspNetRoles" PRIMARY KEY ("Id")
);

CREATE TABLE "AspNetUsers" (
    "Id" text NOT NULL,
    "UserName" character varying(256) NULL,
    "NormalizedUserName" character varying(256) NULL,
    "Email" character varying(256) NULL,
    "NormalizedEmail" character varying(256) NULL,
    "EmailConfirmed" boolean NOT NULL,
    "PasswordHash" text NULL,
    "SecurityStamp" text NULL,
    "ConcurrencyStamp" text NULL,
    "PhoneNumber" text NULL,
    "PhoneNumberConfirmed" boolean NOT NULL,
    "TwoFactorEnabled" boolean NOT NULL,
    "LockoutEnd" timestamp with time zone NULL,
    "LockoutEnabled" boolean NOT NULL,
    "AccessFailedCount" integer NOT NULL,
    "Name" text NULL,
    "Family" text NULL,
    "City" text NULL,
    "Bio" text NULL,
    "BirthDay" timestamp without time zone NOT NULL,
    CONSTRAINT "PK_AspNetUsers" PRIMARY KEY ("Id")
);

CREATE TABLE "Associations" (
    "Id" uuid NOT NULL,
    "LastModifiedDate" timestamp without time zone NOT NULL,
    "CreatedDate" timestamp without time zone NOT NULL,
    "Id1" uuid NOT NULL,
    "Id2" uuid NOT NULL,
    "AssociationType" integer NOT NULL,
    "Data" jsonb NULL,
    CONSTRAINT "PK_Associations" PRIMARY KEY ("Id")
);

CREATE TABLE "Objects" (
    "Id" uuid NOT NULL,
    "LastModifiedDate" timestamp without time zone NOT NULL,
    "CreatedDate" timestamp without time zone NOT NULL,
    "ObjectId" uuid NOT NULL,
    "Type" smallint NOT NULL,
    "Data" jsonb NULL,
    CONSTRAINT "PK_Objects" PRIMARY KEY ("Id")
);

CREATE TABLE "Places" (
    "Id" uuid NOT NULL,
    "LastModifiedDate" timestamp without time zone NOT NULL,
    "CreatedDate" timestamp without time zone NOT NULL,
    "EnglishName" text NULL,
    "Location" geography NULL,
    "Name" text NULL,
    "Address" text NULL,
    "Avatar" text NULL,
    "Rate" real NOT NULL,
    "Attributes" jsonb NULL,
    "Discriminator" text NOT NULL,
    "Breakfast" boolean NULL,
    "Lunch" boolean NULL,
    "Dinner" boolean NULL,
    "Saturday" jsonb NULL,
    "Sunday" jsonb NULL,
    "Monday" jsonb NULL,
    "Tuesday" jsonb NULL,
    "Wednesday" jsonb NULL,
    "Thursday" jsonb NULL,
    "Friday" jsonb NULL,
    CONSTRAINT "PK_Places" PRIMARY KEY ("Id")
);

CREATE TABLE "ReviewModel" (
    "Id" uuid NOT NULL,
    "LastModifiedDate" timestamp without time zone NOT NULL,
    "CreatedDate" timestamp without time zone NOT NULL,
    "Rate" integer NOT NULL,
    "Title" text NULL,
    CONSTRAINT "PK_ReviewModel" PRIMARY KEY ("Id")
);

CREATE TABLE "AspNetRoleClaims" (
    "Id" integer NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    "RoleId" text NOT NULL,
    "ClaimType" text NULL,
    "ClaimValue" text NULL,
    CONSTRAINT "PK_AspNetRoleClaims" PRIMARY KEY ("Id"),
    CONSTRAINT "FK_AspNetRoleClaims_AspNetRoles_RoleId" FOREIGN KEY ("RoleId") REFERENCES "AspNetRoles" ("Id") ON DELETE CASCADE
);

CREATE TABLE "AspNetUserClaims" (
    "Id" integer NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    "UserId" text NOT NULL,
    "ClaimType" text NULL,
    "ClaimValue" text NULL,
    CONSTRAINT "PK_AspNetUserClaims" PRIMARY KEY ("Id"),
    CONSTRAINT "FK_AspNetUserClaims_AspNetUsers_UserId" FOREIGN KEY ("UserId") REFERENCES "AspNetUsers" ("Id") ON DELETE CASCADE
);

CREATE TABLE "AspNetUserLogins" (
    "LoginProvider" text NOT NULL,
    "ProviderKey" text NOT NULL,
    "ProviderDisplayName" text NULL,
    "UserId" text NOT NULL,
    CONSTRAINT "PK_AspNetUserLogins" PRIMARY KEY ("LoginProvider", "ProviderKey"),
    CONSTRAINT "FK_AspNetUserLogins_AspNetUsers_UserId" FOREIGN KEY ("UserId") REFERENCES "AspNetUsers" ("Id") ON DELETE CASCADE
);

CREATE TABLE "AspNetUserRoles" (
    "UserId" text NOT NULL,
    "RoleId" text NOT NULL,
    CONSTRAINT "PK_AspNetUserRoles" PRIMARY KEY ("UserId", "RoleId"),
    CONSTRAINT "FK_AspNetUserRoles_AspNetRoles_RoleId" FOREIGN KEY ("RoleId") REFERENCES "AspNetRoles" ("Id") ON DELETE CASCADE,
    CONSTRAINT "FK_AspNetUserRoles_AspNetUsers_UserId" FOREIGN KEY ("UserId") REFERENCES "AspNetUsers" ("Id") ON DELETE CASCADE
);

CREATE TABLE "AspNetUserTokens" (
    "UserId" text NOT NULL,
    "LoginProvider" text NOT NULL,
    "Name" text NOT NULL,
    "Value" text NULL,
    CONSTRAINT "PK_AspNetUserTokens" PRIMARY KEY ("UserId", "LoginProvider", "Name"),
    CONSTRAINT "FK_AspNetUserTokens_AspNetUsers_UserId" FOREIGN KEY ("UserId") REFERENCES "AspNetUsers" ("Id") ON DELETE CASCADE
);

CREATE TABLE "RefreshToken" (
    "Id" uuid NOT NULL,
    "UserModelId" text NOT NULL,
    "LastModifiedDate" timestamp without time zone NOT NULL,
    "CreatedDate" timestamp without time zone NOT NULL,
    "Token" text NULL,
    "Expires" timestamp without time zone NOT NULL,
    "Created" timestamp without time zone NOT NULL,
    "CreatedByIp" text NULL,
    "Revoked" timestamp without time zone NULL,
    "RevokedByIp" text NULL,
    "ReplacedByToken" text NULL,
    CONSTRAINT "PK_RefreshToken" PRIMARY KEY ("UserModelId", "Id"),
    CONSTRAINT "FK_RefreshToken_AspNetUsers_UserModelId" FOREIGN KEY ("UserModelId") REFERENCES "AspNetUsers" ("Id") ON DELETE CASCADE
);

CREATE TABLE "Posts" (
    "Id" uuid NOT NULL,
    "LastModifiedDate" timestamp without time zone NOT NULL,
    "CreatedDate" timestamp without time zone NOT NULL,
    "Text" text NULL,
    "UserId" text NULL,
    "PlaceId" uuid NOT NULL,
    CONSTRAINT "PK_Posts" PRIMARY KEY ("Id"),
    CONSTRAINT "FK_Posts_Places_PlaceId" FOREIGN KEY ("PlaceId") REFERENCES "Places" ("Id") ON DELETE CASCADE,
    CONSTRAINT "FK_Posts_AspNetUsers_UserId" FOREIGN KEY ("UserId") REFERENCES "AspNetUsers" ("Id") ON DELETE RESTRICT
);

CREATE TABLE "ReviewModel_Medias" (
    "ReviewModelId" uuid NOT NULL,
    "Id" integer NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    "MediaType" integer NOT NULL,
    "MimeType" text NULL,
    "Path" text NULL,
    "Size" integer NOT NULL,
    "Name" text NULL,
    CONSTRAINT "PK_ReviewModel_Medias" PRIMARY KEY ("ReviewModelId", "Id"),
    CONSTRAINT "FK_ReviewModel_Medias_ReviewModel_ReviewModelId" FOREIGN KEY ("ReviewModelId") REFERENCES "ReviewModel" ("Id") ON DELETE CASCADE
);

CREATE TABLE "Posts_Medias" (
    "PostModelId" uuid NOT NULL,
    "Id" integer NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    "MediaType" integer NOT NULL,
    "MimeType" text NULL,
    "Path" text NULL,
    "Size" integer NOT NULL,
    "Name" text NULL,
    CONSTRAINT "PK_Posts_Medias" PRIMARY KEY ("PostModelId", "Id"),
    CONSTRAINT "FK_Posts_Medias_Posts_PostModelId" FOREIGN KEY ("PostModelId") REFERENCES "Posts" ("Id") ON DELETE CASCADE
);

CREATE INDEX "IX_AspNetRoleClaims_RoleId" ON "AspNetRoleClaims" ("RoleId");

CREATE UNIQUE INDEX "RoleNameIndex" ON "AspNetRoles" ("NormalizedName");

CREATE INDEX "IX_AspNetUserClaims_UserId" ON "AspNetUserClaims" ("UserId");

CREATE INDEX "IX_AspNetUserLogins_UserId" ON "AspNetUserLogins" ("UserId");

CREATE INDEX "IX_AspNetUserRoles_RoleId" ON "AspNetUserRoles" ("RoleId");

CREATE INDEX "EmailIndex" ON "AspNetUsers" ("NormalizedEmail");

CREATE UNIQUE INDEX "UserNameIndex" ON "AspNetUsers" ("NormalizedUserName");

CREATE INDEX "IX_Places_Location" ON "Places" ("Location");

CREATE INDEX "IX_Posts_PlaceId" ON "Posts" ("PlaceId");

CREATE INDEX "IX_Posts_UserId" ON "Posts" ("UserId");

